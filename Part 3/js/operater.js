//Switch
var num8 = parseInt(prompt("Enter a number between 1-4"));
switch (num8) {
    case 1:
        document.write("This is case 1");
        break;
    case 2:
        document.write("This is case 2");
        break;
    case 3:
        document.write("This is case 3");
        break;
    case 4:
        document.write("This is case 4");
        break;
    default:
        document.write("This is default case");
        break;
}

